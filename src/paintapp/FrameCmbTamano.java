    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String lado=jComboBox1.getSelectedItem().toString();
        int agregar;
        String agregarStr=jTextField1.getText();
        try{
            agregar=Integer.valueOf(agregarStr);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Ponga un numero valido");
            return;
        }
        if (agregar>=-limiteEspacioAgregar && agregar<=limiteEspacioAgregar){
            if (control.cambiarTamanoLienzo(lado, agregar)){
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se ha podido modificar el tamano\n"
                        + "Asegurec de que la dimension no quede fuera de los limites");
            }
        } else {
            JOptionPane.showMessageDialog(this, "El limite es entre -"+ limiteEspacioAgregar +" y "+limiteEspacioAgregar);
        }
    }//GEN-LAST:event_jButton1ActionPerformed



