    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ancho;
        int alto;

        try {
            ancho = Integer.valueOf(jTextField1.getText());
            alto = Integer.valueOf(jTextField2.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ponga un numero valido");
            return;
        }

        if (!isDimensionesValidas(ancho, alto)) {
            return;
        }

        FramePaint frame = new FramePaint(ancho, alto);
        frame.setExtendedState(FramePaint.MAXIMIZED_BOTH);
        frame.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        File cargar;
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setMultiSelectionEnabled(false);
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileFilter filtro = new FileNameExtensionFilter("Imagenes PNG", "png");
        fileChooser.setFileFilter(filtro);
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            cargar = fileChooser.getSelectedFile();
            Color[][] imagen = ControlGuardado.cargarImagen(cargar);
            if (imagen != null) {
                if (imagen.length < MAXIMO_DIMENSION && imagen[0].length < MAXIMO_DIMENSION) {
                    FramePaint frame = new FramePaint(imagen.length, imagen[0].length);
                    frame.setExtendedState(FramePaint.MAXIMIZED_BOTH);
                    frame.setImagenGuardada(imagen, cargar);
                    frame.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Dimensiones no validas\n"
                            + "La dimension mas larga tien que tener\n"
                            + "un maximo de " + MAXIMO_DIMENSION + " pixeles");
                }
            } else {
                JOptionPane.showMessageDialog(this, "No se pundo cargar la imagen");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private boolean isDimensionesValidas(int ancho, int alto) {
        if (ancho < 1 || ancho > MAXIMO_DIMENSION || alto < 1 || alto > MAXIMO_DIMENSION) {
            JOptionPane.showMessageDialog(this, "Dimensiones no validas\n"
                    + "La dimension mas larga tien que tener\n"
                    + "un maximo de " + MAXIMO_DIMENSION + " pixeles");
            return false;
        } else {
            return true;
        }
    }

